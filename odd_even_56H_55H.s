		AREA MYCODE,CODE,READONLY
		ENTRY
		EXPORT __main
__main
		LDR R4,=DATA_ARR
		MOV R5,#10
		
MAIN_LOOP
		LDR R0,[R4],#4
		BL CHECK_ODD
		SUBS R5,R5,#1
		BNE MAIN_LOOP
		
STOP
		B STOP
		
CHECK_ODD
		TST R0,#1
		BEQ EVEN 
		LDR R6,=ODD_ADDR
		STRB R0,[R6]
		BX LR

EVEN 
		LDR R6,=EVEN_ADDR
		STRB R0,[R6]
		BX LR
		
		AREA MYDATA,DATA,READONLY
DATA_ARR  DCD 11,22,33,44,55,66,77,88,99,100
ODD_ADDR  DCB 0x55
EVEN_ADDR DCB 0x56

END